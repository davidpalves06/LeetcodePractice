var chalkReplacer = function(chalk, k) {
    let chalkSum = chalk.reduce((accumulator,value) => accumulator + value,0);
    while (k >= chalkSum) k-= chalkSum;
    while (k != 0) {
        for (let i = 0; i < chalk.length; i++) {
            if (chalk[i] > k) {
                return i;
            }
            k -= chalk[i]
        }
    }

    return 0;

    
};

